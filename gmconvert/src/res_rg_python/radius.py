def set_radius_to_pdb(rad,pdb):
  pdb.radius = [0.0 for i in range(pdb.Natom)]
  for i in range(pdb.Natom):
    atom = pdb.atom[i]
    resi = pdb.resi[i]
    if (atom[1]!='H'):
      if (resi in rad) and (atom in rad[resi]):
        pdb.radius[i] = rad[resi][atom]
    #print "'%s' '%s' radius %.2f"%(pdb.resi[i],pdb.atom[i],pdb.radius[i])
  pass


def set_weight_to_pdb(pdb):
#  These values are taken from
#  Urabyoushi I. Genshiryou-hyou (1985)
#  in "Rikagakujiten dai-4-ban".
  sum_weight = 0.0
  pdb.weight = [0.0 for i in range(pdb.Natom)]
  for i in range(pdb.Natom):
    atom = pdb.atom[i]
    ele = atom[1]
    if (ele!='H'):
      if (ele=='C'):
        pdb.weight[i] = 12.011
      elif (ele=='N'):
        pdb.weight[i] = 14.00674
      elif (ele=='O'):
        pdb.weight[i] = 15.9994
      elif (ele=='S'):
        pdb.weight[i] = 32.066 
      elif (ele=='P'):
        pdb.weight[i] = 30.973762 
      else:
        pdb.weight[i] = 12.011 
    sum_weight += pdb.weight[i]
  return(sum_weight)


